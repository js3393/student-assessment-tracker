
class Student:
    def __init__(self, date, student_id, first_name, last_name,
                 grade_level, age, overall_grade, teacher_name, attendance, project_progress):
        self.date = date
        self.id = student_id
        self.first_name = first_name
        self.last_name = last_name
        self.grade_level = grade_level
        self.age = age
        self.overall_grade = overall_grade
        self.teacher_name = teacher_name
        self.attendance = attendance
        self.project_progress = project_progress
        
    @staticmethod
    def get_id(self, student_id):
        return self.student_id

    @staticmethod
    def get_overall_grade(self, overall_grade):
        return self.overall_grade

    @staticmethod
    def get_attendance(self, attendance):
        return self.attendance

    @staticmethod
    def get_project_progress(self):
        return self.project_progress




